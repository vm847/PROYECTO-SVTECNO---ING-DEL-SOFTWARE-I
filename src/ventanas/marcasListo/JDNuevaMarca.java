    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String codigo = this.txtCodigo.getText();
        String nombre = this.txtNombreMarca.getText();
        if(!nombre.isEmpty()){
            if(!bd.verificarDuplicidadString("marcas", "nombre_marca", nombre, "cod_marca", codigo)){
                String campos = "nombre_marca, usuario_insercion, fecha_insercion";
                String valores = "'" + nombre + "', '" + usuarioLogeado + "', NOW()";
                bd.insertarRegistro("marcas", campos, valores);
                frmMarcas.actualizarGrilla();
                this.dispose();
                JOptionPane.showMessageDialog(null, "Nueva Marca Guardada", "Registro Guardada", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null, "Esta marca ya esta registrada");
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

